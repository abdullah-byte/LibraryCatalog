public class libraryCata{
    public String addBook;

    public String removeBook;

    public int findBook;

    private String listBook;

    public  libraryCata(){


    }


    public String getAddBook() {
        return addBook;
    }

    public void setAddBook(String addBook) {
        this.addBook = addBook;
    }

    public String getRemoveBook() {
        return removeBook;
    }

    public void setRemoveBook(String removeBook) {
        this.removeBook = removeBook;
    }


    public int getFindBook() {
        return findBook;
    }

    public void setFindBook(int findBook) {
        this.findBook = findBook;
    }

    public String getListBook() {
        return listBook;
    }

    public void setListBook(String listBook) {
        this.listBook = listBook;
    }
}
